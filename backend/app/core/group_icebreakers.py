"""Темы и ледоколы для групповых комнат (MVP: статические пулы)."""

from __future__ import annotations

import random

WEEKLY_THEMES = [
    "Неделя честности: о чём вы обычно умалчиваете в первый раз?",
    "Медленные разговоры: что помогает вам лучше думать — тишина или фон?",
    "Границы: как вы говорите «нет», не чувствуя вину?",
    "Маленькие радости: без чего ваш день кажется пустым?",
    "Перегруз: как вы восстанавливаетесь после людей?",
]

DAILY_PROMPTS = [
    "Один книжный/фильмовый тезис, с которым вы согласны на 80% — какой?",
    "Что вы в последний раз меняли в своих привычках и зачем?",
    "Город или природа — где вам проще дышать и почему?",
    "Какой навык, не связанный с работой, вы бы выучили в идеале?",
    "Честный ответ: что вас раздражает в «мелких разговорах»?",
    "Что вы делаете, когда не знаете, что сказать?",
    "Один совет, который вам часто дали, но он вам не подошёл?",
]

COMMUNITY_RULES = [
    "Это дружеские комнаты MatchMe — не для романтики и не «свиданий». Уважайте платонический формат.",
    "Уважайте границы: не персональные атаки, не дискриминация.",
    "Спам и реклама запрещены; ссылки — по делу.",
    "Никто не обязан отвечать сразу; давайте время.",
    "Почта и личные контакты не публикуйте — только псевдонимы из профиля.",
]

PRIVACY_NOTICE = (
    "В группе видны только ваш псевдоним (имя из профиля) и сообщения. "
    "Email и другие данные аккаунта участникам не показываются."
)

PLATONIC_MISSION = (
    "Группы MatchMe подбираются по близости взглядов на осях — это про дружбу и общий контекст, "
    "не про романтику. Небольшие комнаты снижают давление «первого сообщения один на один»."
)


def cohort_size_note(min_members: int, max_members: int) -> str:
    return (
        f"Размер комнаты обычно от {min_members} до {max_members} человек — намеренно небольшой, "
        "как в формате «три на кофе», чтобы было проще войти в разговор."
    )


def pick_weekly_theme() -> str:
    return random.choice(WEEKLY_THEMES)


def pick_daily_prompt() -> str:
    return random.choice(DAILY_PROMPTS)
